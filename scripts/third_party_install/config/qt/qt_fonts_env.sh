#!/usr/bin/env bash
# ==============================================================================
# qt_fonts_env.sh - Qt 字体环境变量配置
# ==============================================================================
# 此脚本定义 Qt 应用程序在嵌入式 Linux 上正确显示字体所需的环境变量
#
# 使用方法：
#   1. Source 此脚本: source qt_fonts_env.sh
#   2. 或添加到 /etc/environment
#   3. 或在应用启动脚本中添加
#
# 字体目录结构（由 install_fonts.sh 创建）：
#   /usr/share/fonts/
#   ├── dejavu/
#   │   ├── DejaVuSans.ttf
#   │   ├── DejaVuSans-Bold.ttf
#   │   └── DejaVuSansMono.ttf
#   ├── noto/
#   │   └── NotoSansCJK-Regular.ttc
#   └── emoji/
#       └── NotoColorEmoji.ttf
# ==============================================================================

# ================================================================
# Qt 字体环境变量
# ================================================================

# Qt 字体目录（告诉 Qt 在哪里查找字体）
export QT_QPA_FONTDIR="/usr/share/fonts"

# 语言环境（确保中文正确显示）
export LANG="C.UTF-8"
export LC_ALL="C.UTF-8"

# Qt 平台插件（可选，根据实际平台配置）
# export QT_QPA_PLATFORM="linuxfb"

# ================================================================
# 字体 fallback 顺序（Qt 自动处理）
# ================================================================
# Qt 会按以下顺序自动选择字体：
# 1. Latin 字符  → DejaVu Sans
# 2. 中日韩字符  → Noto Sans CJK
# 3. Emoji       → Noto Color Emoji
# 4. 等宽字体    → DejaVu Sans Mono
#
# 无需额外配置，Qt 会自动根据字符选择合适的字体
# ================================================================

# ================================================================
# 在 C++ 代码中使用字体
# ================================================================
# // 设置默认字体
# QFont font("Noto Sans CJK SC", 12);
# QApplication::setFont(font);
#
# // 中英混排文本
# QLabel* label = new QLabel("Hello 世界 🎉");
# label->setFont(QFont("Noto Sans CJK SC", 20));
#
# // 等宽字体（终端）
# QFont monoFont("DejaVu Sans Mono", 10);
# ================================================================

